public class p5
{

    float getaveragee(float p)
    {
        float avg;
        avg = p;
        return avg;
    }

    float getaverage(float p, float q)
    {
        float avg;
        avg = (p + q) / 2;
        return avg;
    }

    float getaverage(float p, float q, float r)     // getaverage method find average
    {
        float avg;
        avg = (p + q + r) / 3;
        return avg;
    }
}
